/**
 * List of valid entries
 * @author Matt Wipfler
 */
public enum SupportedUnits {

    // Length/distance
    FEET,
    METER,
    MILE,
    KILOMETER,

    // Weight/mass
    POUND,
    OUNCE,
    GRAM,
    KILOGRAM,

    // Volume
    GALLON,
    QUART,
    LITER,

    // Temperature
    CELSIUS,
    FAHRENHEIT,

    // Default
    VOID
}
